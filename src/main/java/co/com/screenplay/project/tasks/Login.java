package co.com.screenplay.project.tasks;

import co.com.screenplay.project.ui.HomeUI;
import net.serenitybdd.screenplay.Actor;
import net.serenitybdd.screenplay.Task;
import net.serenitybdd.screenplay.actions.Click;
import net.serenitybdd.screenplay.actions.Enter;

import static net.serenitybdd.screenplay.Tasks.instrumented;

public class Login implements Task {
    private final String username;
    private final String password;

    public Login(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public static Login conCredenciales(String username, String password) {
        return instrumented(Login.class, username, password);
    }

    @Override
    public <T extends Actor> void performAs(T actor) {
        actor.attemptsTo(
                Enter.theValue(username).into(HomeUI.TXT_USERNAME),
                Enter.theValue(password).into(HomeUI.TXT_PASSWORD),
                Click.on(HomeUI.BTN_LOGIN)
        );
    }
}
